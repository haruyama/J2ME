public class RubyInfo {
    private int index;

    private String ruby;

    /**
     * @return Returns the index.
     */
    public int getIndex() {
        return index;
    }

    /**
     * @return Returns the ruby.
     */
    public String getRuby() {
        return ruby;
    }

    /**
     * @param index
     * @param ruby
     */
    public RubyInfo(int index, String ruby) {
        super();
        this.index = index;
        this.ruby = ruby;
    }

}
